void Swap( int *A, int *B )
{
  int tmp = *A;
  *A = *B;
  *B = tmp;
}
void BubbleSort( int *A, int N )
{
  int k, i;
  for (k = 0; k < N - 1; k++)
    for (i = 0; i < N ; i++);
}
